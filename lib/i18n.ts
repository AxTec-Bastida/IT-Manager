export type TranslationNamespace = keyof typeof dictionaries.en;

function interpolate(value: string, params?: TranslationParams) {
  if (!params) return value;
  return value.replace(/\{(\w+)\}/g, (match, key) => String(params[key] ?? match));
}

export function t(locale: Locale, key: string, params?: TranslationParams) {
  const [namespace, itemKey] = key.split(".");
  if (!namespace || !itemKey) return key;
  const dictionaryView = dictionaries as Record<Locale, Record<string, Record<string, string | undefined>>>;
  const localized = dictionaryView[locale]?.[namespace]?.[itemKey];
  const fallback = dictionaryView.en[namespace]?.[itemKey];
  return interpolate(localized ?? fallback ?? key, params);
}

export function createTranslator(locale: Locale, namespace: TranslationNamespace) {
  return (key: string, params?: TranslationParams) => t(locale, `${namespace}.${key}`, params);
}

export function getNamespace(locale: Locale, namespace: TranslationNamespace) {
  const dictionaryView = dictionaries as Record<Locale, Record<string, Record<string, string | undefined>>>;
  const fallbackNamespace = dictionaryView.en[namespace] ?? {};
  return new Proxy(fallbackNamespace, {
    get(target, property) {
      if (typeof property !== "string") return undefined;
      return dictionaryView[locale]?.[namespace]?.[property] ?? target[property] ?? property;
    },
  }) as Record<string, string>;
}

